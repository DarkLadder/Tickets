public class Calc {
    int a, b, c, d, e, f;
    int i;

    public Calc(int a, int b, int c, int d, int e, int f) {
        this.a = a;
        this.b = b;
        this.c = c;
        this.d = d;
        this.e = e;
        this.f = f;
    }
    public void Calcul(){
        if ((a + b + c) == (d + e + f)) i++;
    }
}
